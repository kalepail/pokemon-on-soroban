// Drawer: paints a sprite at (x, y) with optional scale + flipX.
function drawSprite(ctx, sprite, palette, x, y, scale = 1, flipX = false) {
  const sx = Math.round(x);
  const sy = Math.round(y);
  for (let row = 0; row < sprite.length; row++) {
    const line = sprite[row];
    for (let col = 0; col < line.length; col++) {
      const c = line[col];
      if (c === '.') continue;
      const idx = parseInt(c, 16);
      const color = palette[idx];
      if (!color || color === 'transparent') continue;
      ctx.fillStyle = color;
      const drawCol = flipX ? (line.length - 1 - col) : col;
      ctx.fillRect(
        sx + Math.round(drawCol * scale),
        sy + Math.round(row * scale),
        Math.ceil(scale),
        Math.ceil(scale),
      );
    }
  }
}

// Sprite dimensions (assumes uniform).
function spriteW(sprite) { return sprite[0].length; }
function spriteH(sprite) { return sprite.length; }

